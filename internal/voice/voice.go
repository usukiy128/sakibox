func init() {
	rand.Seed(time.Now().UnixNano())
}

func Line(key string) string {
	options, ok := lines[key]
	if !ok || len(options) == 0 {
		return key
	}
	return options[rand.Intn(len(options))]
}

func Linef(key string, args ...interface{}) string {
	return fmt.Sprintf(Line(key), args...)
}

